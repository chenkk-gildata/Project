SELECT B.XXLY 信息来源,B.XXFBRQ 信息发布日期,B.ZQDM 证券代码,B.INBBM 内部编码,B.BDLB 变动类型,ID
FROM
(SELECT XXLY,JGDM,XXFBRQ,INBBM,ZQDM,BDLB,ID
FROM
(SELECT XXLY,JGDM,XXFBRQ,INBBM,ZQDM,CASE WHEN BDLB=10 THEN '1' WHEN BDLB=20 THEN '2' END BDLB,
ROW_NUMBER() OVER (PARTITION BY JGDM,INBBM,BDLB ORDER BY XXFBRQ DESC) RANK,ID
FROM [10.101.0.212].JYPRIME.dbo.usrRZRQBDZQ
WHERE INBBM IN (SELECT INBBM FROM [10.101.0.212].JYPRIME.dbo.usrZQZB WHERE ZQSC IN (83,90) AND SSZT=5) AND JGDM IN (25403,25404,41127,41169,41136,1052,41126,2726,41086))A
WHERE A.RANK=1)B
LEFT JOIN
(SELECT XXLY,JGDM,XXFBRQ,INBBM,BDLX
FROM [10.101.0.212].JYPRIME.dbo.usrRZRQBDZQZT
WHERE BDLX IN (1,2))C
ON B.JGDM=C.JGDM AND B.INBBM=C.INBBM AND B.BDLB=C.BDLX
WHERE C.JGDM IS NULL
ORDER BY B.XXFBRQ DESC