SELECT CONVERT(VARCHAR,GETDATE(),120)






WITH #LSB AS(
SELECT DISTINCT B.GPDM,CONVERT(DATE,A.XXFBRQ) XXFBRQ,CONVERT(DATE,A.JZRQ) JZRQ,A.YJYJDX,
	CONVERT(DECIMAL(18,2),A.YJFDQSPL) YJFDQSPL,CONVERT(DECIMAL(18,2),A.YJFDJZPL) YJFDJZPL,CONVERT(DECIMAL(18,2),A.QSZJE) QSZJE,
	CONVERT(DECIMAL(18,2),A.JZZJE) JZZJE,CONVERT(DECIMAL(18,2),A.QSJE) QSJE,CONVERT(DECIMAL(18,2),A.JZJE) JZJE,
	C.YYZSR,C.YYSR,C.KCHYYSR,C.JLRHJ,C.JLR,C.KCFJYXSYHDJLR
FROM [10.106.22.60].JYFIN.dbo.usrCWJCZBZYCYJYG A 
	JOIN [10.101.0.212].JYPRIME.dbo.usrZQZB B ON A.INBBM=B.INBBM AND B.ZQSC IN (18,83,90) AND B.ZQLB IN (1,2,41)
	JOIN [10.101.0.212].JYPRIME.dbo.usrGSCWZYZB C ON A.INBBM=C.INBBM AND A.XXFBRQ=C.XXFBRQ AND DATEADD(YY,-1,A.JZRQ)=C.JZRQ
WHERE A.YJYJLX='FCC000001E1F' AND A.YJYJDX IN ('CFI000000036','CFI0000000KL','CIS000000001','CIS000000002','CIS00000001X','CIS000000020') AND C.RQBZ=3
)
SELECT GPDM,XXFBRQ,JZRQ,
	CASE WHEN YJYJDX='CIS000000001' THEN '营业总收入'
		WHEN YJYJDX='CIS000000002' THEN '营业收入'
		WHEN YJYJDX='CFI0000000KL' THEN '扣除后营业收入'
		WHEN YJYJDX='CIS00000001X' THEN '净利润'
		WHEN YJYJDX='CIS000000020' THEN '归母净利润'
		WHEN YJYJDX='CFI000000036' THEN '扣除后净利润'
	END AS 'YJYJDX',
	YJFDQSPL,YJFDJZPL,QSZJE,JZZJE,QSJE,JZJE,
	CASE WHEN YJYJDX='CIS000000001' THEN YYZSR
		WHEN YJYJDX='CIS000000002' THEN YYSR
		WHEN YJYJDX='CFI0000000KL' THEN KCHYYSR
		WHEN YJYJDX='CIS00000001X' THEN JLRHJ
		WHEN YJYJDX='CIS000000020' THEN JLR
		WHEN YJYJDX='CFI000000036' THEN KCFJYXSYHDJLR
	END AS '主要指标'
FROM #LSB
WHERE ((YJYJDX='CIS000000001' AND QSJE/YYZSR BETWEEN 0.98 AND 1.02 AND YYZSR IS NOT NULL)
	OR
	(YJYJDX='CIS000000002' AND QSJE/YYSR BETWEEN 0.98 AND 1.02 AND YYSR IS NOT NULL)
	OR
	(YJYJDX='CFI0000000KL' AND QSJE/KCHYYSR BETWEEN 0.98 AND 1.02 AND KCHYYSR IS NOT NULL)
	OR
	(YJYJDX='CIS00000001X' AND QSJE/JLRHJ BETWEEN 0.98 AND 1.02 AND JLRHJ IS NOT NULL)
	OR
	(YJYJDX='CIS000000020' AND QSJE/JLR BETWEEN 0.98 AND 1.02 AND JLR IS NOT NULL)
	OR
	(YJYJDX='CFI000000036' AND QSJE/KCFJYXSYHDJLR BETWEEN 0.98 AND 1.02 AND KCFJYXSYHDJLR IS NOT NULL))





