SELECT B.GPDM,CONVERT(DATE,A.XXFBRQ) XXFBRQ,C.MS,COUNT(*) 数据量,D.OPERATORNAME
INTO #XRLX
FROM usrGSCWZYZB A
	JOIN usrZQZB B ON A.INBBM=B.INBBM AND B.ZQSC IN (18,83,90) AND B.ZQLB IN (1,2,41)
	JOIN usrXTCLB C ON A.XXLYBM=C.DM AND C.LB=2181
	JOIN cmdOPERATORS D ON A.XGRY=D.OPERATORID
WHERE A.XGSJ>'2025-02-24' AND A.XGRY IN ('50106647')
GROUP BY B.GPDM,A.XXFBRQ,D.OPERATORNAME,C.MS

