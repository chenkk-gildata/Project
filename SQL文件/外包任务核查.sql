--领导人介绍任务公告量提取语句							
SELECT A.ID 审核表ID,CONVERT(DATE,A.CLRQ) 处理日期,CONVERT(DATE,A.GGRQ) 公告日期,F.GPDM 股票代码,A.CLRY,							
    CASE WHEN E.DM=198 AND A.GGLB = 36 AND A.ZQJC LIKE '%年报处理%' THEN '定报上下任'
         WHEN E.DM=198 AND A.GGLB = 50 AND isnull(A.ZQJC,'其他') NOT LIKE '%日常委员%'THEN '换届'
         WHEN E.DM=198 AND A.GGLB = 36 AND isnull(A.ZQJC,'其他') NOT LIKE '%日常委员%' THEN '非换届'
         WHEN E.DM=198 AND A.GGLB = 50 AND A.ZQJC LIKE '%日常委员%' THEN '委员换届'
         WHEN E.DM=198 AND A.GGLB = 36 AND A.ZQJC LIKE '%日常委员%' THEN '委员非换届'
         WHEN E.DM=198 AND A.GGLB = 39 THEN '无需处理公告'
    END 类型
INTO #LDRJS
FROM [10.101.0.212].JYPRIME.dbo.usrSJCBSHB A
    JOIN [10.101.0.212].JYPRIME.dbo.usrZQZB F ON A.INBBM =F.INBBM
    JOIN [10.101.0.212].JYPRIME.dbo.usrSJCBSHB_SL E ON E.ID = A.ID AND E.LB = 1
WHERE E.DM IN (198) AND A.GGLB IN (50,39,36) AND (ISNULL(A.ZQJC,'其他') NOT LIKE '%委员%' OR A.ZQJC  LIKE '%日常委员会%' )
    AND A.CLRY NOT IN ('50105453','50105697','50104619','50103802','50104591','50105194','50106030','50104942')
    AND CONVERT(DATE,A.CLRQ) >='2025-01-01' AND CONVERT(DATE,A.CLRQ) <='2025-12-31'

DROP TABLE #LDRJS

SELECT * FROM #LDRJS

SELECT 类型,COUNT(1) FROM #LDRJS GROUP BY 类型 ORDER BY 1,2


